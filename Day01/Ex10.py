# 형식을 갖춘 문자열
# %
# %d    : 정수(10진수)
# %0    : 정수(8진수)
# %x    : 정수(16진수)
# %f    : 실수
# %s    : 문자열

a = 10
b = 20
print('%d' % 10)
print('%o' % 10)
print('%x' % 10) # 16진수에서는 10부터 a로 사용됨 123456789abcdef
print('%f' % 10)
print('%s' % 'Python')
print('%d %d' % (a, b))

# 문자열 형식문자 %s 는 정수, 실수 모두 처리할 수 있다.
print('%s' % 100)
print('%s' % 3.14)


